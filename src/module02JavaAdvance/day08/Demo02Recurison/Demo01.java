package module02JavaAdvance.day08.Demo02Recurison;

public class Demo01 {
    private int age;
    private String name;

    public Demo01(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public Demo01() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
